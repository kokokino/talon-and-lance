// Repeat a set of layer patterns cyclically to fill the desired count.
// Each layer is deep-copied to avoid shared references.
function repeatLayers(patterns, count) {
  const result = [];
  for (let i = 0; i < count; i++) {
    result.push(patterns[i % patterns.length].map(r => [...r]));
  }
  return result;
}

